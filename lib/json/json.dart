class UserRes {
  final int id;
  final String name;
  final String username;
  final String email;
  final Map<String, dynamic> address;
  final String phone;
  final String website;
  final Map<String, dynamic> company;

  UserRes(
      {required this.id,
      required this.name,
      required this.username,
      required this.email,
      required this.address,
      required this.phone,
      required this.website,
      required this.company});

  factory UserRes.fromJson(Map<String, dynamic> json) {
    return UserRes(
      id: json['id'],
      name: json['name'],
      username: json['username'],
      email: json['email'],
      address: json['address'],
      phone: json['phone'],
      website: json['website'],
      company: json[CompanyRes] as Map<String, dynamic>,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'name': name,
      'username': username,
      'email': email,
      'address': address,
      'phone': phone,
      'website': website,
      'company': company,
    };
  }
}

class AddressRes {
  final String street;
  final String suite;
  final String city;
  final String zipcode;
  final Map<String, dynamic> geo;

  AddressRes(
      {required this.street,
      required this.suite,
      required this.city,
      required this.zipcode,
      required this.geo});

  factory AddressRes.fromJson(Map<String, dynamic> json) {
    return AddressRes(
      street: json['street'] as String,
      suite: json['suite'] as String,
      city: json['city'] as String,
      zipcode: json['zipcode'] as String,
      geo: json[GeoRes] as Map<String, dynamic>,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'street': street,
      'suite': suite,
      'city': city,
      'zipcode': zipcode,
      'geo': geo,
    };
  }
}

class GeoRes {
  final String lat;
  final String lng;

  GeoRes({required this.lat, required this.lng});

  factory GeoRes.fromJson(Map<String, dynamic> json) {
    return GeoRes(
      lat: json['lat'] as String,
      lng: json['lng'] as String,
    );
  }
}

class CompanyRes {
  final String name;
  final String catchPhrase;
  final String bs;

  CompanyRes({required this.name, required this.catchPhrase, required this.bs});

  factory CompanyRes.fromJson(Map<String, dynamic> json) {
    return CompanyRes(
      name: json['name'] as String,
      catchPhrase: json['catchPhrase'] as String,
      bs: json['bs'] as String,
    );
  }
}

